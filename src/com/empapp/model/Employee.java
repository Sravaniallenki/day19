package com.empapp.model;

public class Employee {

     private int status;
     private int dailywage;
     private int workhrs;

     public int EmployeeStatus(){
            this.status=status;
            return status;
     }

     public int EmployeeWage(){
            this.dailywage=dailywage;
            return dailywage;
     }

     public int Employee_workhrs(){
            this.workhrs=workhrs;
            return workhrs;
     }

}
